package class2;

public class VariableValues {

    public static void main(String[] args) {

        byte primData = 126;
        System.out.println(primData);
        primData = 127;
        System.out.println(primData);

        short primData2 = 1099;
        System.out.println(primData2);
        primData2 = 2000;
        System.out.println(primData2);

        int primData3 = 80000;
        System.out.println(primData3);
        primData3 = 800001;
        System.out.println(primData3);

        long primData4 = 1111222222233333L;
        System.out.println(primData4);
        primData4 = 1111111111111L;
        System.out.println(primData4);


        float primData5 = 12.23F;
        System.out.println(primData5);
        primData5 = 12.24F;
        System.out.println(primData5);

        double primData6 = 3.1452645582;
        System.out.println(primData6);
        primData6 = 3.456574;
        System.out.println(primData6);


        char primData7 = 'M';
        System.out.println(primData7);
        primData7 = 'F';
        System.out.println(primData7);

        boolean primData8 = false;
        System.out.println(primData8);
        primData8 = true;
        System.out.println(primData8);
    }
}
