package class2;
/*
Create a Java program (any class with a main method) and name it Practice Variable
In your program create 8 variables to store all different types of primitive data
Print the value of each variable
 */
public class T1PracticeVariables {

    public static void main(String[] args) {
        // Only whole numbers
        byte verySmallWholeNumberBox = 39;
        short smallWholeNumberBox = 1099;
        int mostWidelyUsedWholeNumberBox = 80000;
        long largeWholeNBox = 1111222222233333L;

        // Decimals
        float mediumDecimalBox = 12.23F;
        double largeDecimalBox = 3.1452645582;

        // characters
        char singleCharacterBox = 'M';
        boolean trueFalseBox = false;


        System.out.println(verySmallWholeNumberBox);
        System.out.println(smallWholeNumberBox);
        System.out.println(mostWidelyUsedWholeNumberBox);
        System.out.println(largeWholeNBox);
        System.out.println(singleCharacterBox);
        System.out.println(trueFalseBox);
        System.out.println(mediumDecimalBox);
        System.out.println(largeDecimalBox );
    }
}
