package class3;

public class E7RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10 > 15); // is 10 greater than 15
        System.out.println(10 < 15); // is 10 less than 15
        System.out.println(10 == 15); // is 10 equal to 15
        System.out.println(10 != 15); // is 10 not equal to 15
        System.out.println(10 >= 15); // is 10 greater than or equal to 15
        System.out.println(10 <= 15); // is 10 less than or equal to 15
    }
}
